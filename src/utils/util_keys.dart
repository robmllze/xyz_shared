import 'package:flutter/material.dart' show ValueKey;

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class UriKey extends ValueKey<Uri?> {
  const UriKey([Uri? uri]) : super(uri);
  Uri? get uri => this.value;
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

String combinedOrderedStringId(List<String> ids) {
  final sorted = ids..sort((final a, final b) => a.compareTo(b));
  final combined = sorted.join("_");
  return combined;
}
