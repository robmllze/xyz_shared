import 'package:flutter/material.dart';

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class MyStatefulBuilder extends StatefulWidget {
  final Widget Function(BuildContext context, _State state) builder;
  const MyStatefulBuilder({
    Key? key,
    required this.builder,
  }) : super(key: key);

  @override
  _State createState() => _State();
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class _State extends State<MyStatefulBuilder> {
  void refresh([void Function()? fn]) {
    this.setState(fn ?? () {});
  }

  @override
  Widget build(final context) => this.widget.builder(context, this);
}
