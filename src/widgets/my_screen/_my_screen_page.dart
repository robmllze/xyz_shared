class MyTransparentPage extends MaterialPage {
  //
  //
  //

  MyTransparentPage({
    Widget? child,
  }) : super(
          key: UniqueKey(),
          child: SizedBox(child: child),
        );

  //
  //
  //

  String? get location => super.name;

  //
  //
  //

  @override
  Route createRoute(_) {
    return _TransparentRoute(settings: this, builder: (_) => this.child);
  }
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class MyScreenPage extends MaterialPage {
  //
  //
  //

  MyScreenPage({
    required LocalKey key,
    required MyScreen child,
  }) : super(
          key: key,
          child: child,
          name: child.configuration.uri.toString(),
        );

  //
  //
  //

  String? get location => super.name;

  //
  //
  //

  MyScreen get screen => this.child as MyScreen;

  //
  //
  //

  @override
  Route createRoute(BuildContext context) {
    if (this.screen.makeup.isTranslucent == true) {
      return _TransparentRoute(settings: this, builder: (_) => this.child);
    } else {
      return _PageBasedMaterialPageRoute(page: this);
    }
  }
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

// Copied from Flutter source code.
class _PageBasedMaterialPageRoute<T> extends PageRoute<T> with MaterialRouteTransitionMixin<T> {
  _PageBasedMaterialPageRoute({
    required MaterialPage<T> page,
  }) : super(settings: page) {
    assert(opaque);
  }

  MaterialPage<T> get _page => settings as MaterialPage<T>;

  @override
  Widget buildContent(BuildContext context) {
    return _page.child;
  }

  @override
  bool get maintainState => _page.maintainState;

  @override
  bool get fullscreenDialog => _page.fullscreenDialog;

  @override
  String get debugLabel => '${super.debugLabel}(${_page.name})';
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class _TransparentRoute extends PageRoute<void> {
  _TransparentRoute({
    required this.builder,
    required RouteSettings settings,
  }) : super(settings: settings, fullscreenDialog: false);

  final WidgetBuilder builder;

  @override
  bool get opaque => false;

  @override
  Color? get barrierColor => null;

  @override
  String? get barrierLabel => null;

  @override
  bool get maintainState => true;

  @override
  Duration get transitionDuration => const Duration(milliseconds: 350);

  @override
  Widget buildPage(
    BuildContext context,
    Animation<double> animation,
    Animation<double> secondaryAnimation,
  ) {
    final result = builder(context);
    return FadeTransition(
      opacity: Tween<double>(begin: 0, end: 1).animate(animation),
      child: Semantics(
        scopesRoute: true,
        explicitChildNodes: true,
        child: result,
      ),
    );
  }
}
