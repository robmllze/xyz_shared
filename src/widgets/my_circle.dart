import 'package:flutter/material.dart';

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class MyCircle extends StatelessWidget {
  //
  //
  //

  final Widget child;
  final double? diameter;

  //
  //
  //

  const MyCircle({
    Key? key,
    required this.child,
    this.diameter,
  }) : super(key: key);

  //
  //
  //

  @override
  Widget build(_) {
    return Container(
      width: this.diameter,
      height: this.diameter,
      decoration: const BoxDecoration(shape: BoxShape.circle),
      clipBehavior: Clip.antiAlias,
      child: this.child,
    );
  }
}
