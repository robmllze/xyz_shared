// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class MyOmniScrollable extends StatelessWidget {
  //
  //
  //

  final Widget child;

  //
  //
  //

  const MyOmniScrollable({Key? key, required this.child}) : super(key: key);

  //
  //
  //

  @override
  Widget build(_) {
    return SingleChildScrollView(
      scrollDirection: Axis.horizontal,
      physics: const BouncingScrollPhysics(),
      child: SingleChildScrollView(
        scrollDirection: Axis.vertical,
        physics: const BouncingScrollPhysics(),
        child: this.child,
      ),
    );
  }
}
